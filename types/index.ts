export type ItineraryItemCat = 'travel' | 'stay' | 'food' | 'beach' | 'activity';

export interface LatLng {
  lat: number;
  lng: number;
}

export interface ItineraryItem {
  id: string;
  /** Minutes since midnight (0–1439), or null when not yet scheduled. See lib/time.ts. */
  time: number | null;
  title: string;
  place: string;
  url?: string;
  cat: ItineraryItemCat;
  who: UserId;
  coords?: LatLng;
  /** ISO date the range ends: check-out for stays, return for flights. */
  endDate?: string;
  likes: number;
  liked: boolean;
  comments: number;
}

export interface Day {
  id: string;
  n: number;
  /** Display label, e.g. "Oct 3". */
  date: string;
  /** Real calendar date, ISO yyyy-mm-dd (missing on older data). */
  iso?: string;
  title: string;
  area: string;
  items: ItineraryItem[];
}

export interface ItineraryItemInput {
  /** "HH:mm" from <input type="time">. */
  time?: string;
  title: string;
  place?: string;
  cat: ItineraryItemCat;
  url?: string;
  lat?: number;
  lng?: number;
  /** ISO date: check-out (stay) or return (travel). */
  endDate?: string;
}

export interface ItineraryApi {
  listDays(tripId: string): Promise<Day[]>;
  setupDays(tripId: string, groupId: string, start: string, end: string): Promise<void>;
  addItem(dayId: string, input: ItineraryItemInput): Promise<void>;
  updateItem(itemId: string, input: ItineraryItemInput): Promise<void>;
  removeItem(itemId: string): Promise<void>;
  toggleLike(itemId: string, liked: boolean): Promise<void>;
}

