// A destination suggestion generated by the AI for any trip (as opposed to
// the seeded Lombok demo catalog referenced by cardIds).
export interface AiSuggestion {
  title: string;
  area: string;
  detail: string;
  price?: string;
  kind: 'stay' | 'eat' | 'activity' | 'travel';
  /** Suggested start time, "HH:mm" 24h. */
  time?: string;
  /** Approximate WGS84 coordinates of the place. */
  lat?: number;
  lng?: number;
}

// ── Events: lightweight single-date outings proposed to the group ────────────

export interface EventPart {
  id?: string;        // absent for parts not yet saved
  title: string;
  time?: string;
  venue?: string;
  venueUrl?: string;
  ticketUrl?: string;
}

export interface GroupEvent {
  id: string;
  title: string;
  date?: string;      // ISO yyyy-mm-dd
  time?: string;      // free text, e.g. "7pm"
  note?: string;
  venue?: string;
  venueUrl?: string;
  ticketUrl?: string;
  tint: string;
  who: UserId;
  going: UserId[];
  parts: EventPart[];
}

export interface EventInput {
  title: string;
  date?: string;
  time?: string;
  note?: string;
  venue?: string;
  venueUrl?: string;
  ticketUrl?: string;
  parts?: EventPart[];
}

export interface EventsApi {
  list(groupId: string): Promise<GroupEvent[]>;
  add(groupId: string, input: EventInput): Promise<void>;
  update(eventId: string, input: EventInput): Promise<void>;
  rsvp(eventId: string, going: boolean): Promise<void>;
  remove(eventId: string): Promise<void>;
}

